void solve() {
  int n, m;
  cin >> n >> m;
  int s = 0;
  while (n--) {
    int x;
    cin >> x;
    s += x;
  }
  if (s == m) {
    cout << "YES\n";
  } else {
    cout << "NO\n";
  }
}

