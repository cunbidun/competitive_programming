vector<int> prefix_function(string s) {
  int n = (int)s.length();
  vector<int> pi(n);
  for (int i = 1; i < n; i++) {
    int j = pi[i - 1];
    while (j > 0 && s[i] != s[j])
      j = pi[j - 1];
    if (s[i] == s[j])
      j++;
    pi[i] = j;
  }
  return pi;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  int T;
  cin >> T;
  while (T--) {
    string s;
    cin >> s;
    string ans = "";
    int mn = 1e9;
    vector<int> a;
    for (int i = 0; i < (int)s.size(); i++) {
      a.push_back(i);
    }
    do {
      string tmp = "";
      for (int i : a) {
        tmp += s[i];
      }
      vector<int> v = prefix_function(tmp);
      int mx = *max_element(v.begin(), v.end());
      if (mx < mn) {
        ans = tmp;
        mn = mx;
      } else if (mx == mn) {
        ans = min(ans, tmp);
      }
    } while (next_permutation(a.begin(), a.end()));
    cout << ans << '\n';
  }
}
